public class Transaccion {

	private String traDatos;	
	private String sCOD;
	private String sDESCOD;
	private Object result;
	
	
	public String getTraDatos() {
		return traDatos;
	}
	public void setTraDatos(String traDatos) {
		this.traDatos = traDatos;
	}
	public String getsCOD() {
		return sCOD;
	}
	public void setsCOD(String sCOD) {
		this.sCOD = sCOD;
	}
	public String getsDESCOD() {
		return sDESCOD;
	}
	public void setsDESCOD(String sDESCOD) {
		this.sDESCOD = sDESCOD;
	}
	public Object getResult() {
		return result;
	}
	public void setResult(Object result) {
		this.result = result;
	}
	
	
	
	
}
