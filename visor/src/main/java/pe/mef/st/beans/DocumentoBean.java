package pe.mef.st.beans;

public class DocumentoBean {
	
	private int idDocumento;
	private String tipodoc;
	private String documento;
	private String razonsocial;
	private String apepat;
	private String apemat;
	private String nombres;
	private String documentoname;
	private String path;
	private String documentofisico;
	
	public int getIdDocumento() {
		return idDocumento;
	}
	public void setIdDocumento(int idDocumento) {
		this.idDocumento = idDocumento;
	}
	public String getTipodoc() {
		return tipodoc;
	}
	public void setTipodoc(String tipodoc) {
		this.tipodoc = tipodoc;
	}
	public String getDocumento() {
		return documento;
	}
	public void setDocumento(String documento) {
		this.documento = documento;
	}
	public String getRazonsocial() {
		return razonsocial;
	}
	public void setRazonsocial(String razonsocial) {
		this.razonsocial = razonsocial;
	}
	public String getApepat() {
		return apepat;
	}
	public void setApepat(String apepat) {
		this.apepat = apepat;
	}
	public String getApemat() {
		return apemat;
	}
	public void setApemat(String apemat) {
		this.apemat = apemat;
	}
	public String getNombres() {
		return nombres;
	}
	public void setNombres(String nombres) {
		this.nombres = nombres;
	}
	public String getDocumentoname() {
		return documentoname;
	}
	public void setDocumentoname(String documentoname) {
		this.documentoname = documentoname;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public String getDocumentofisico() {
		return documentofisico;
	}
	public void setDocumentofisico(String documentofisico) {
		this.documentofisico = documentofisico;
	}
	
	
	
}
